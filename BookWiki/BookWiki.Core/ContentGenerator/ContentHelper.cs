        private static Random _rnd = new Random();

        public static string GenerateParagraph()
        {
            var list = new List<string>();

            var firstWord = true;

            for (int i = 0; i < 200; i++)
            {
                var word = GetWord(firstWord);

                if (firstWord)
                {
                    firstWord = false;
                }

                if (_rnd.Next(0, 10) > 8)
                {
                    list.Add(word + ".");

                    firstWord = true;
                }
                else
                {
                    list.Add(word);
                }
            }

            list.Add("\n");

            return string.Join(" ", list.ToArray());
        }

        private static string GetWord(bool capital)
        {
            var wordIndex = _rnd.Next(0, _words.Length - 1);

            var word = _words[wordIndex];

            if (capital)
            {
                return char.ToUpper(word[0]) + word.Substring(1);
            }
            else
            {
                return word;
            }
        }

